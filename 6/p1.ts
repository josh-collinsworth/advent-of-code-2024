type Coordinates = [number, number];
type Direction = (typeof DIRECTIONS)[number];

const GUARD_SYMBOLS = ["^", "v", "<", ">"];
const DIRECTIONS = ["up", "down", "left", "right"];
const map = realInput.split("\n").map((row) => row.split(""));

let visitedPositions: Coordinates[] = [];
let currentlyHeading: Direction = "up";
let guardIsMoving = true;

const findGuardPosition = (): Coordinates => {
  let guardPosition: Coordinates = [-1, -1];
  map.forEach((row, rowIndex) => {
    row.forEach((col, colIndex) => {
      if (GUARD_SYMBOLS.includes(col)) {
        guardPosition = [rowIndex, colIndex];
      }
    });
  });
  return guardPosition;
};

const getNextSpaceCoordinates = (): Coordinates => {
  const [row, col] = findGuardPosition()!;
  if (currentlyHeading === "up") {
    return [row - 1, col];
  } else if (currentlyHeading === "down") {
    return [row + 1, col];
  } else if (currentlyHeading === "left") {
    return [row, col - 1];
  } else {
    return [row, col + 1];
  }
};

const getCurrentGuardSymbol = (): string => {
  if (currentlyHeading === "up") {
    return "^";
  } else if (currentlyHeading === "down") {
    return "v";
  } else if (currentlyHeading === "left") {
    return "<";
  } else {
    return ">";
  }
};

const getNextSpaceContent = (): string => {
  const [nextRow, nextCol] = getNextSpaceCoordinates();
  return map[nextRow]?.[nextCol];
};

const rotateGuard = () => {
  if (currentlyHeading === "right") {
    currentlyHeading = "down";
  } else if (currentlyHeading === "down") {
    currentlyHeading = "left";
  } else if (currentlyHeading === "left") {
    currentlyHeading = "up";
  } else {
    currentlyHeading = "right";
  }
};

while (guardIsMoving) {
  visitedPositions.push(findGuardPosition());
  let nextSpace = getNextSpaceContent();
  if (nextSpace === ".") {
    const [nextRow, nextCol] = getNextSpaceCoordinates();
    const [guardRow, guardCol] = findGuardPosition();
    map[guardRow][guardCol] = ".";
    map[nextRow][nextCol] = getCurrentGuardSymbol();
  } else if (nextSpace === "#") {
    rotateGuard();
  } else {
    guardIsMoving = false;
  }
}

const visitedPositionsSet = new Set(
  visitedPositions.map((pos) => pos.join(","))
);

